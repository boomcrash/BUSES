    private void textoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textoKeyTyped
       char letra = evt.getKeyChar();
        if(!Character.isLetter(letra)
            && letra != KeyEvent.VK_BACK_SPACE
            && letra != KeyEvent.VK_SPACE
            && letra != KeyEvent.VK_DELETE) {
            //    JOptionPane.showMessageDialog(this, "SOLO DIGITE LETRAS", "ATENCIÃ“N", JOptionPane.WARNING_MESSAGE);
            evt.consume();
        }     
    }//GEN-LAST:event_textoKeyTyped

    private void filtrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filtrarActionPerformed
        if(filtro.getSelectedItem().toString().trim().equalsIgnoreCase("todos")){
            admin.llenarTabla(tabla);
        }else if(filtro.getSelectedItem().toString().trim().equalsIgnoreCase("origen")){
            admin.filtrarTabla(tabla,"origen",texto.getText().toString().trim());
        }else if(filtro.getSelectedItem().toString().trim().equalsIgnoreCase("destino")){
            admin.filtrarTabla(tabla,"destino",texto.getText().toString().trim());
        }
        
    }//GEN-LAST:event_filtrarActionPerformed


